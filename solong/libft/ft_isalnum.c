#include "libft.h"

int	ft_isalnum(int num)
{
	if (num >= 48 && num <= 57)
		return (1);
	else if (num >= 65 && num <= 90)
		return (1);
	else if (num >= 97 && num <= 122)
		return (1);
	else
		return (0);
}
